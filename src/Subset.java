public class Subset {
    public static void main(String[] args) {
        int k = Integer.parseInt(args[0]);
        StdOut.println(k);

        String[] strs = StdIn.readAllStrings();
        for (String s: strs) {
            StdOut.println(s);
        }
    }
}
